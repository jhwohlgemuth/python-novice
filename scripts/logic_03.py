# loops_03.py


def main():
    # initialize a variable "x"
    x = 1

    # check to see if "x" is less than 1, 2, or 3
    if x < 1:
        print("x is less than 1")
    elif x < 2:
        print("x is less than 2")
    elif x < 3:
        print("x is less than 3")
    else:
        print("x is not less than 1, 2, or 3")


if __name__ == "__main__":
    main()
